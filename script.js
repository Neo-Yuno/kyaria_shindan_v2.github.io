function initQuiz() {
  state = {
    screen: "quiz",
    currentIndex: 0,
    answers: [],
    branch: null,
    questions: [...commonQuestions]
  };
  showScreen("quiz");
  renderQuestion();
}

function showScreen(screen) {
  el.startScreen.classList.toggle("hidden", screen !== "start");
  el.quizScreen.classList.toggle("hidden", screen !== "quiz");
  el.resultScreen.classList.toggle("hidden", screen !== "result");
}

function renderQuestion() {
  const q = state.questions[state.currentIndex];
  el.questionTitle.textContent = q.text;
  el.phaseLabel.textContent = state.currentIndex < commonQuestions.length ? "共通質問" : `分岐質問：${getBranchLabel(state.branch)}`;
  el.currentNumber.textContent = state.currentIndex + 1;
  el.totalNumber.textContent = state.questions.length;
  el.progressBar.style.width = `${(state.currentIndex / state.questions.length) * 100}%`;
  el.backBtn.disabled = state.currentIndex === 0;
  el.answers.innerHTML = "";

  answerOptions.forEach(option => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "answer-btn";
    btn.innerHTML = `<span>${option.label}</span><span class="points">${option.value}点</span>`;
    btn.addEventListener("click", () => answerQuestion(option.value));
    el.answers.appendChild(btn);
  });
}

function answerQuestion(value) {
  const q = state.questions[state.currentIndex];
  state.answers[state.currentIndex] = {
    questionId: q.id,
    axis: q.axis,
    value
  };

  const nextIndex = state.currentIndex + 1;

  if (nextIndex === commonQuestions.length && !state.branch) {
    state.branch = decideBranch(calculateScores(state.answers));
    state.questions = [...commonQuestions, ...branchQuestions[state.branch]];
  }

  if (nextIndex >= state.questions.length) {
    renderResult();
    return;
  }

  state.currentIndex = nextIndex;
  renderQuestion();
}

function goBack() {
  if (state.currentIndex === 0) return;
  state.answers.pop();
  state.currentIndex -= 1;

  if (state.currentIndex < commonQuestions.length) {
    state.branch = null;
    state.questions = [...commonQuestions];
  }

  renderQuestion();
}

function calculateScores(answers) {
  const scores = { R: 0, I: 0, A: 0, S: 0, E: 0, C: 0 };
  answers.forEach(answer => {
    scores[answer.axis] += answer.value;
  });
  return scores;
}

function decideBranch(scores) {
  const tech = scores.R + scores.I;
  const people = scores.S + scores.E;
  const create = scores.A + scores.C;

  const max = Math.max(tech, people, create);
  if (max === tech) return "tech";
  if (max === people) return "people";
  return "create";
}

function getBranchLabel(branch) {
  if (branch === "tech") return "技術・研究";
  if (branch === "people") return "対人・推進";
  if (branch === "create") return "表現・管理";
  return "";
}

function getTopPair(scores) {
  const sorted = Object.entries(scores).sort((a, b) => b[1] - a[1]);
  const pair = [sorted[0][0], sorted[1][0]].sort().join("");
  const normalized = normalizePair(pair);
  return { code: normalized, topAxes: [sorted[0][0], sorted[1][0]], sorted };
}

function normalizePair(pair) {
  const order = ["R", "I", "A", "S", "E", "C"];
  return pair.split("").sort((a, b) => order.indexOf(a) - order.indexOf(b)).join("");
}

