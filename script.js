function copyResult(profile, scores, topAxes) {
  const { code } = getTopPair(scores);
  const recommendations = mergeRecommendations(code, topAxes);

  const text = [
    `診断結果：${profile.name}`,
    profile.summary,
    "",
    "RIASECスコア：",
    ...Object.entries(scores).map(([axis, value]) => `${AXIS_LABELS[axis]}：${value}`),
    "",
    "向いている職種：",
    ...profile.jobs.map(job => `・${job}`),
    "",
    "集中した方がいい授業：",
    ...recommendations.classes.map(([priority, title, reason]) => `・${title}（${priority}）：${reason}`),
    "",
    "取った方がいい資格：",
    ...recommendations.certs.map(([priority, title, reason]) => `・${title}（${priority}）：${reason}`),
    "",
    "大学中にやるとよいこと：",
    ...profile.actions.map(([title, body]) => `・${title}：${body}`)
  ].join("\n");

  const showCopied = () => {
    el.copyResultBtn.textContent = "コピーしました";
    setTimeout(() => {
      el.copyResultBtn.textContent = "結果をコピー";
    }, 1400);
  };

  const showFailed = () => {
    el.copyResultBtn.textContent = "コピーできませんでした";
    setTimeout(() => {
      el.copyResultBtn.textContent = "結果をコピー";
    }, 1800);
  };

  if (navigator.clipboard && window.isSecureContext) {
    navigator.clipboard.writeText(text).then(showCopied).catch(() => {
      fallbackCopyText(text) ? showCopied() : showFailed();
    });
    return;
  }

  fallbackCopyText(text) ? showCopied() : showFailed();
}

function fallbackCopyText(text) {
  const textarea = document.createElement("textarea");
  textarea.value = text;
  textarea.setAttribute("readonly", "");
  textarea.style.position = "fixed";
  textarea.style.left = "-9999px";
  document.body.appendChild(textarea);
  textarea.select();

  let copied = false;
  try {
    copied = document.execCommand("copy");
  } catch (error) {
    copied = false;
  }

  document.body.removeChild(textarea);
  return copied;
}

